export type OwnerPortalLanguage = keyof typeof ownerPortalTranslations;
export type OwnerPortalTranslationKeys = keyof typeof ownerPortalTranslations.en;

/**
 * Detect preferred language from browser
 * Returns 'nl' if Dutch is detected, otherwise 'en' (default)
 */
export function detectOwnerPortalLanguage(): OwnerPortalLanguage {
  if (typeof window === "undefined") return "en";
  
  const browserLang = navigator.language || (navigator as any).userLanguage || "";
  
  // Check if Dutch is the browser language
  if (browserLang.toLowerCase().startsWith("nl")) {
    return "nl";
  }
  
  return "en";
}

/**
 * Get translations for a specific language
 */
export function getOwnerPortalTranslations(lang: OwnerPortalLanguage) {
  return ownerPortalTranslations[lang] || ownerPortalTranslations.en;
}

